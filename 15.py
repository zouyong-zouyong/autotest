# 结果：[1, 2, 3, 5, 6, 7, 8]  其中的下标3位置的4已取走

#二维数组  概念 
x=[1,2,3,["一","二","三百"],(11,22,33)]    #数组里有数组和元组  (二位数组)
print(len(x))    #结果： 5，共计5个值，开始数时是0-4，len（X）的括号不能用中括号
print(x)         #结果: 全部显示了
print(x[3])      #结果： ['一', '二', '三百']
print(x[3][2])   #结果: 三百

#用list 将字符串输出成数组，
x1=list("asdfghgfhjghj")  #用list 将字符串输出成数组
print(x1)                   #结果；['a', 's', 'd', 'f', 'g', 'h', 'g', 'f', 'h', 'j', 'g', 'h', 'j']

x2=tuple("asdfghgfhjghj")#用 tupie 将字符串输出成元组
print(x2)                    #结果；('a', 's', 'd', 'f', 'g', 'h', 'g', 'f', 'h', 'j', 'g', 'h', 'j')



#                 字 典
x3={"a":1,
    "b":22,
    "c":"张三",
    "f":{
        "Z":"3",
        "X":"李四",
        "Q":"总数"
    }   
 }
a1=x3.get("b")    # get 是取走某个值，
print(a1)         # 结果；22 

a2=x3.pop("a")    # pop 是取走某个值，
print(a2)         # 结果；1 

del x3["c"]        #  del是删除
print(x3)          # 结果：{'b': 22, 'f': {'Z': '3', 'X': '李四', 'Q': '总数'}}
                   #接上一行，删除了“C”=张三

x3["q"]="加快学习"   # 替换，将变量“q’的值的替换掉。
