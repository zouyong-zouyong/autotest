#数组
# append只能加到数组结尾；insert可以加到如何位


xx=[1,2,3,4,5,"hh","张三","李四"] #数组
xx.append("新加的内容") #加到了数组的末尾， append（啊盆的）方法。
print(xx)
xx.insert(0,"后加的东西")
print(xx)  #输出结果；加到了“0”的位置
xx.insert(3,"不加的东西")
print(xx)  #输出结果；加到了“3”的位置或改加到任意位置。

#  pop  通过 下标 取走数组里面的内容
